- "我注意到..."
- "你像是..."
- "也许..."
- "我先听见的是..."
- "这句话里，有个地方很重。"
- "它可能不大，但它确实压在你这里。"
- "我不急着替你判断。"
- "我们先把它放在这里。"
- "你刚才一直在说别人，但我也看到了你。"

